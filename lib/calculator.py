def add(x: int, y: int) -> int:
    """
    덧셈

    Args:
        x: 입력값 1
        y: 입력값 2

    Returns:
        덧셈 값

    """
    print(x + y)
    return x + y

def subtract(x: int, y: int) -> int:
    """
    뺄셈
    
    Args:
        x: 입력값 1
        y:  입력값 2

    Returns:
        뺄셈 값

    """
    print(x - y)
    return x - y

def multiply(x: int, y: int) -> int:
    """
    곱하기

    Args:
        x: 입력값 1
        y: 입력값 2

    Returns:
        곱한 값

    """
    print(x * y)
    return x * y

def divide(x: int, y: int) -> float:
    """
    나누기
    
    Args:
        x: 입력값1
        y: 입력값2

    Returns:
        나눈 값

    """
    print(x / y)
    return x / y